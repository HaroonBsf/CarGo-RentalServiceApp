package com.example.fyp.models;

public class ReadWriteUserDetails {

    public String name, company, email, cnic, phone;

    public ReadWriteUserDetails(String name, String company, String email, String cnic, String phone) {
        this.name = name;
        this.company = company;
        this.email = email;
        this.cnic = cnic;
        this.phone = phone;
    }
}

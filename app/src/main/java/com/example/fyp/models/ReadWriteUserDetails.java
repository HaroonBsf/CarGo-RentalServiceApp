package com.example.fyp.models;

public class ReadWriteUserDetails {

    public String name, company, email, cnic, phone, id, status;

    public ReadWriteUserDetails(String name, String company, String email, String cnic, String phone, String id, String status) {
        this.name = name;
        this.company = company;
        this.email = email;
        this.cnic = cnic;
        this.phone = phone;
        this.id = id;
        this.status = status;
    }
}
